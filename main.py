estado = input('Digite seu estado de nascimento: ')

if estado == 'rio de janeiro':
    print('Você é carioca')
elif estado == 'sao paulo':
    print('Você é paulista')
elif estado == 'minas gerais':
    print('Você é mineiro')
elif estado == 'bahia':
    print('Você é baiano')
elif estado == 'ceara':
    print('Você é cearense')
else:
    print('Você é de outro estado')
